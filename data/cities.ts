/**
 * Her süre seçeneği bir HALKA (ring) gösterir:
 * minDistanceKm < mesafe ≤ maxDistanceKm
 * Böylece 12 saat seçen kullanıcı Mardin gibi yakın şehirleri görmez,
 * sadece gerçekten 12 saatlik uzaklıktaki başkentleri görür.
 */
export function getReachableDestinations(
  departure: City,
  durationOption: FlightDurationOption
): City[] {
  const minKm = durationOption.minDistanceKm;
  const maxKm = durationOption.maxDistanceKm;
  // İlk kalkış Türkiye'den → Türkiye içi varış yok
  // Devam uçuşlarında (yurt dışı kalkış) → her ülke varış olabilir
  const blockTR = departure.countryCode === "TR";

  return CITIES.filter((c) => {
    if (c.id === departure.id) return false;
    if (blockTR && c.countryCode === "TR") return false;
    const dist = haversineKm(departure.lat, departure.lng, c.lat, c.lng);
    return dist > minKm && dist <= maxKm;
  }).sort(
    (a, b) =>
      haversineKm(departure.lat, departure.lng, a.lat, a.lng) -
      haversineKm(departure.lat, departure.lng, b.lat, b.lng)
  );
}

export function haversineKm(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number
): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

export function flagEmoji(countryCode: string): string {
  return countryCode
    .toUpperCase()
    .split("")
    .map((c) => String.fromCodePoint(c.charCodeAt(0) + 0x1f1a5))
    .join("");
}
